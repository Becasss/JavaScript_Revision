// Addition Assignment Operators

let x = 10;
x += 5; // x = x + 5
document.write(x); 



// Subraction Assignment Operators

let y = 20;
y -= 10; // y = y - 10
document.write("<br>" + y);


// Multiplication Assignment Operators

let z = 5;
z *= 10; // z = z * 10
document.write("<br>" + z);

//  Division Assignment Operators

let w = 100;
w /= 5; // w = w / 5
document.write("<br>" + w);

// Exponentiation Assignment Operators

let a = 2;
a **= 3; // a = a ** 3
document.write("<br>" + a);

