// Addition Assignment Operators

let x = 10;
x += 5; // x = x + 5
document.write(x); 



// Subraction Assignment Operators

let y = 20;
y -= 10; // y = y - 10
document.write("<br>" + y);


// Multiplication Assignment Operators

let z = 5;
z *= 10; // z = z * 10
document.write("<br>" + z);

//  Division Assignment Operators

let w = 100;
w /= 5; // w = w / 5
document.write("<br>" + w);

// Exponentiation Assignment Operators

let a = 2;
a **= 3; // a = a ** 3
document.write("<br>" + a);

// Remainder Assignment Operators

let b = 10;
b %= 3; // b = b % 3
document.write("<br>" + b);

// Bitwise AND Assignment Operators
let v = 100;
v &= 5; // v = v & 5
document.write("<br>" + v);


// Bitwise OR Assignment Operators

let u = 10;
u |= 5; // u = u | 5
document.write("<br>" + u);

// Bitwise XOR Assignment Operators

let t = 10;
t ^= 5; // t = t ^ 5
document.write("<br>" + t);

// Bitwise Left Shift Assignment Operators

let s = 10;
s <<= 2; // s = s << 2
document.write("<br>" + s);

// Bitwise Right Shift Assignment Operators

let r = 10;
r >>= 2; // r = r >> 2
document.write("<br>" + r);

// Bitwise Zero Fill Right Shift Assignment Operators

let q = 10;
q >>>= 2; // q = q >>> 2
document.write("<br>" + q);

// Combined Assignment Operators

let p = 10;
p += 5; // p = p + 5
p -= 10; // p = p - 10
p *= 10; // p = p * 10
p /= 5; // p = p / 5