// The new Set() Method - to creat a set

const letters = new Set([1, 2, 3, 4, 5]);

// Display set.size

console.log(`The set has ${letters.size} keys.`);


// Create a set and add values:

const numbers = new Set();

/// Add values to the set
numbers.add("1");
numbers.add(2);
numbers.add(3);
numbers.add(4);
numbers.add(5);

// Display the set


console.log(numbers);
console.log(numbers.size);
